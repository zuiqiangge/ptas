package com.pengyue.ptas.bean;

public class SoftWare {
	private String id;
	private String name;
	private String macId;
	private String version;
	private String createTime;
	private String finalUpdateTime;
	private String isNew;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMacId() {
		return macId;
	}
	public void setMacId(String macId) {
		this.macId = macId;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public String getFinalUpdateTime() {
		return finalUpdateTime;
	}
	public void setFinalUpdateTime(String finalUpdateTime) {
		this.finalUpdateTime = finalUpdateTime;
	}
	public String getIsNew() {
		return isNew;
	}
	public void setIsNew(String isNew) {
		this.isNew = isNew;
	}
	
	
	
}
