package com.pengyue.ptas.bean;

public class SxOrder {
	private String id;
	private String sxzzId;
	private String sxContent;
	private String time;
	private Integer status;
	private String teacher;
	private String place;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getSxzzId() {
		return sxzzId;
	}
	public void setSxzzId(String sxzzId) {
		this.sxzzId = sxzzId;
	}
	public String getSxContent() {
		return sxContent;
	}
	public void setSxContent(String sxContent) {
		this.sxContent = sxContent;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getTeacher() {
		return teacher;
	}
	public void setTeacher(String teacher) {
		this.teacher = teacher;
	}
	public String getPlace() {
		return place;
	}
	public void setPlace(String place) {
		this.place = place;
	}
	
	
	
}
