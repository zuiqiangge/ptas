package com.pengyue.ptas.bean;

public class SxResource {
	  private String id;
	  private String name;
	  private String sxxmId;
	  private String url;
	  private Integer status;
	  private String type;
	  
	  public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	public String getSxxmId() {
		return sxxmId;
	}
	public void setSxxmId(String sxxmId) {
		this.sxxmId = sxxmId;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	  
	  
	  
}
